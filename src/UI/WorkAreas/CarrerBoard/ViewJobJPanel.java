    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblJobs.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(this, "Please select a Job from the list!","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        Job job = (Job)tblJobs.getValueAt(selectedRow, 0);
        ViewApplicantsJPanel panel = new ViewApplicantsJPanel(userProcessContainer,travel, job);
        userProcessContainer.add("ViewApplicantsJPanel", panel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
    }//GEN-LAST:event_jButton1ActionPerformed


