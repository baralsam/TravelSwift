    private void populateTable(){
        dtm.setRowCount(0);
        for(Job job: travel.getHumResAdmin().getJobPostings()){
            Object[] row = new Object[4];
            row[0] = job;
            row[1] = job.getJobDescription();
            row[2] = job.getJobType();
            row[3] = job.getOrganizationName();
            dtm.addRow(row);
        }
    }

