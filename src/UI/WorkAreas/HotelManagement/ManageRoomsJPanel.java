    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblRoom.getModel();
        dtm.setRowCount(0);
        if (hotel.getRooms() == null) {
            JOptionPane.showMessageDialog(this, "No rooms available in the hotel.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        for (Room room : hotel.getRooms()) {
            Object[] row = new Object[3];
            row[0] = room;
            row[1] = room.getRoomType();
            row[2] = room.getPrice();
            dtm.addRow(row);
        }
    }

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblRoom.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row from table first to delete room", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            Room room = (Room) tblRoom.getValueAt(selectedRow, 0);
            for (Hotel h : travel.getHotelDirectory().getHotelList()) {
                h.deleteRooms(room.getRoomNumber());
                JOptionPane.showMessageDialog(this, "Room removed successfuly!");
                populateTable();
                return;
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

