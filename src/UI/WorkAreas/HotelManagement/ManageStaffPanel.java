    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblStaff.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row from table first to view details", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            Staff staff = (Staff) tblStaff.getValueAt(selectedRow, 0);
            ViewStaffDetailsJPanel panel = new ViewStaffDetailsJPanel(userProcessContainer, travel, staff);
            userProcessContainer.add("ManageStaffDetailsJPanel", panel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblStaff.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row from table first to delete staff", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            Staff staff = (Staff) tblStaff.getValueAt(selectedRow, 0);
            for (Hotel h : travel.getHotelDirectory().getHotelList()) {
                 h.deleteStaff(staff);
                 JOptionPane.showMessageDialog(this, "Staff removed successfuly!");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

