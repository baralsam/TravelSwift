        public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblStaff.getModel();
        dtm.setRowCount(0);
            if (hotel.getStaffList() == null) {
        // Display a warning to the user
        JOptionPane.showMessageDialog(this, "No staff available in the hotel.", "Warning", JOptionPane.WARNING_MESSAGE);
        return; // Exit the method
    }
        for (Staff staff : hotel.getStaffList()) {
            Object[] row = new Object[3];
            row[0] = staff;
            row[1] = staff.getPosition();
            row[2] = staff.getContactInfo();
            dtm.addRow(row);
        }
    }
