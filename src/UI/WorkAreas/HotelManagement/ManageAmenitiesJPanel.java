    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblAmenities.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row from table first to delete amenity", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            String amenities = (String) tblAmenities.getValueAt(selectedRow, 0);
            for (Hotel h : travel.getHotelDirectory().getHotelList()) {
                for (String a : h.getAmenities().getFacilities()) {
                    if (a.equals(amenities)) {
                        h.getAmenities().deleteFacility(amenities);
                         break;
                    }
                   
                }
                JOptionPane.showMessageDialog(this, "Amenity removed successfuly!");
                populateTable();
                return;
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed


