    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblAmenities.getModel();
        dtm.setRowCount(0);
        if (hotel.getAmenities() == null) {
        // Display a warning to the user
        JOptionPane.showMessageDialog(this, "No cuisine available in the hotel.", "Warning", JOptionPane.WARNING_MESSAGE);
        return; // Exit the method
    }
        for (String amenities : hotel.getAmenities().getFacilities()) {
            Object[] row = new Object[1];
            row[0] = amenities;
            dtm.addRow(row);
        }
    }

