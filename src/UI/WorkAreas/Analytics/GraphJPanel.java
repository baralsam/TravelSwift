       DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        dataset.addValue(1.0, "Category 1", "Value 1");
        dataset.addValue(4.0, "Category 1", "Value 2");
        dataset.addValue(3.0, "Category 1", "Value 3");
        dataset.addValue(5.0, "Category 2", "Value 1");

        JFreeChart chart = ChartFactory.createBarChart(
                "Travel Agency Revenue Generated",  // Chart title
                "Travel Agency",           // X-axis label
                "Revenue",              // Y-axis label
                dataset,
                PlotOrientation.VERTICAL,
                false,
                true,
                false
        );

        CategoryPlot barchart = chart.getCategoryPlot();
        barchart.setRangeGridlinePaint(Color.BLUE);
         
        ChartPanel chartPanel = new ChartPanel(chart);
        chartPanel.setDomainZoomable(true);
        jPanel1.removeAll();
        jPanel1.setLayout(new BorderLayout());
        jPanel1.add(chartPanel, BorderLayout.CENTER);
        jPanel1.validate();
