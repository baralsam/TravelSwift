    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblRoutes.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row from table first to view details", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            Route route = (Route) tblRoutes.getValueAt(selectedRow, 0);
            ViewRouteDetailsJPanel panel = new ViewRouteDetailsJPanel(userProcessContainer, travel, route);
            userProcessContainer.add("ViewRouteDetailsJPanel", panel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnViewActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblRoutes.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row from table first to delete route", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            Route route = (Route) tblRoutes.getValueAt(selectedRow, 0);
            for (TransportAgency h : travel.getTrnsportAgencyDirectory().getTransporAgencyList()) {
                h.deleteRoute(route);
                JOptionPane.showMessageDialog(this, "Route removed successfuly!");
                populateTable();
                return;
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

