    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblRoutes.getModel();
        dtm.setRowCount(0);
        if (transportAgency.getRoutes() == null) {
            JOptionPane.showMessageDialog(this, "No routes available in the hotel.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        for (Route ro : transportAgency.getRoutes()) {
            Object[] row = new Object[6];
            row[0] = ro;
            row[1] = ro.getStartLocation();
            row[2] = ro.getEndLocation();
            row[3] = ro.getCapacity();
            row[4] = ro.getPrice();
            row[5] = ro.getPlaneName();
            dtm.addRow(row);
        }
    }    
    
